    // Playlist generating
    private void playlistGeneratorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playlistGeneratorButtonActionPerformed

        workoutType = (String) workoutSelectComboBox.getSelectedItem();

        int playlistSize;

        try {
            playlistSize = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid playlist size!", "Warning", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        disableAllButtons();
        jTextArea1.setText("Loading playlist...");
        jProgressBar1.setVisible(true);
        jProgressBar1.setMinimum(0);
        jProgressBar1.setMaximum(playlistSize);
        jProgressBar1.setValue(0);

        new Thread(new Runnable() {
            @Override
            public void run() {

                String seed = "878dadea-33c5-4c08-bdb9-e2b117475a99";

                double minTempo, maxTempo;
                switch (workoutType) {
                    case "RUNNING":
                        minTempo = 120;
                        maxTempo = 140;
                        break;
                    case "HIIT":
                        minTempo = 160;
                        maxTempo = 180;
                        break;
                    case "CARDIO":
                        minTempo = 110;
                        maxTempo = 130;
                        break;
                    case "YOGA":
                        minTempo = 60;
                        maxTempo = 80;
                        break;
                    case "WEIGHTLIFTING":
                        minTempo = 130;
                        maxTempo = 140;
                        break;
                    case "CYCLING":
                        minTempo = 130;
                        maxTempo = 150;
                        break;
                    case "DANCE":
                        minTempo = 120;
                        maxTempo = 160;
                        break;
                    default:
                        minTempo = 100;
                        maxTempo = 120;
                        break;
                }

                songs = new ArrayList<>();

                for (int i = 0; i < playlistSize; i++) {
                    double targetTempo = minTempo + Math.random() * (maxTempo - minTempo);
                    List<Song> newSongs = ApiService.getSongsByTempo(targetTempo, List.of(seed), 1);

                    if (!newSongs.isEmpty()) {
                        Song candidate = newSongs.get(0);

                        boolean duplicateSong = false;

                        for (Song s : songs) {
                            if (s.getTitle().equals(candidate.getTitle()) && s.getArtist().equals(candidate.getArtist())) {
                                duplicateSong = true;
                                break;
                            }
                        }
                        if (!duplicateSong) {
                            songs.add(candidate);

                            // progress bar 
                            final int loadingBarProgress = songs.size();
                            javax.swing.SwingUtilities.invokeLater(new Runnable() {
                                @Override
                                public void run() {
                                    jProgressBar1.setValue(loadingBarProgress);
                                    jTextArea1.setText("Loading playlist... " + loadingBarProgress + "/" + playlistSize);
                                }
                            });
                        } else {
                            i--;
                            continue;

                        }

                    } else {
                        int choice = javax.swing.JOptionPane.showConfirmDialog(
                                null,
                                "No song found for tempo " + String.format("%.1f", targetTempo) + ". Regenerate?",
                                "Song missing",
                                javax.swing.JOptionPane.YES_NO_OPTION
                        );
                        if (choice != javax.swing.JOptionPane.YES_OPTION) {
                            break;
                        }
                        i--;
                        continue;
                    }
                }

                javax.swing.SwingUtilities.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        jProgressBar1.setVisible(false);
                        originalSongs = new ArrayList<>(songs);
                        jTextField1.setText("Playlist size...");
                        updateOutputArea();
                        enableAllButtons();
                    }
                });

            }
        }).start();

    }//GEN-LAST:event_playlistGeneratorButtonActionPerformed
